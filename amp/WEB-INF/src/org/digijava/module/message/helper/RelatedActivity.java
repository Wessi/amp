package org.digijava.module.message.helper;

public class RelatedActivity {
	private Long actId;
	private String name;
	
	public Long getActId() {
		return actId;
	}
	public void setActId(Long actId) {
		this.actId = actId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
