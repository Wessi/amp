public class LogonSite {
    private String content;
    private String id;
    private String host;
    private String path;

    public LogonSite() {
        this.content = null;
        this.id = null;
        this.host = null;
        this.path = null;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String toString() {
        return "logon-site: content='" + content + "' id='" + id + "'" +
            "' host='" + host + "' path='" + path + "'";
    }

    public String getHost() {
        return SiteUtils.prefixDomainName ( host );
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

}